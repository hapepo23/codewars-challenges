#include <stdio.h>
#include <stdlib.h>
#include <string.h>

char* integerSquareRoot(char* str);

static void do_test(char* str, const char* expected) {
  char* actual = integerSquareRoot(str);
  printf(
      "isqrt(%s):\n"
      "expected: %s\n"
      "actual  : %s\n"
      "%s\n\n",
      str, expected, actual, strcmp(expected, actual) == 0 ? "OK" : "ERROR");
  free(actual);
}

